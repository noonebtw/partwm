pub fn xev_to_mouse_button(
    button: &x11::xlib::XButtonEvent,
) -> Option<MouseButton> {
    match button.button {
        1 => Some(MouseButton::Left),
        2 => Some(MouseButton::Middle),
        3 => Some(MouseButton::Right),
        4 => Some(MouseButton::ScrollUp),
        5 => Some(MouseButton::ScrollDown),
        6 => Some(MouseButton::ScrollLeft),
        7 => Some(MouseButton::ScrollRight),
        8 => Some(MouseButton::Forward),
        9 => Some(MouseButton::Backward),
        _ => None,
    }
}

pub fn mouse_button_to_xbutton(button: MouseButton) -> i32 {
    match button {
        MouseButton::Left => 1,
        MouseButton::Middle => 2,
        MouseButton::Right => 3,
        MouseButton::ScrollUp => 4,
        MouseButton::ScrollDown => 5,
        MouseButton::ScrollLeft => 6,
        MouseButton::ScrollRight => 7,
        MouseButton::Forward => 8,
        MouseButton::Backward => 9,
    }
}

#[derive(Debug, PartialEq, Eq, PartialOrd, Ord, Copy, Clone)]
pub struct XKeysym(u32);

impl Borrow<u32> for XKeysym {
    fn borrow(&self) -> &u32 {
        &self.0
    }
}

impl AsRef<u32> for XKeysym {
    fn as_ref(&self) -> &u32 {
        &self.0
    }
}

impl Deref for XKeysym {
    type Target = u32;

    fn deref(&self) -> &Self::Target {
        &self.0
    }
}

impl From<XKeysym> for VirtualKeyCode {
    fn from(value: XKeysym) -> Self {
        keysym_to_virtual_keycode(*value).unwrap()
    }
}
impl From<VirtualKeyCode> for XKeysym {
    fn from(value: VirtualKeyCode) -> Self {
        Self(virtual_keycode_to_keysym(value).unwrap())
    }
}

